int recurseSuperDigit(char *p)
{
    unsigned long int cnt = 0;
    int len = strlen(p);
    char buf[1024] = {};
    if(len == 1)
        return atoi(p);
    
    for(int i=0; i < len;i++)
    {
        cnt += p[i] - '0';
    }
    sprintf(buf,"%ld",cnt);
    return recurseSuperDigit(buf);
}

int superDigit(char* n, int k) {
    
    int len = strlen(n);
    int ret = 0;
    
    char *p = malloc((unsigned long)len * k + 1); 
    
    memset(p, 0, len * k + 1);
    for(unsigned long int i=0, j=0; i <k; i++, j= j+len)
    {
        memcpy(p+j,n,len);
    }

    ret = recurseSuperDigit(p);
    free(p);
    return ret;
}

